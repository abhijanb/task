import mongoose from "mongoose";

const PropertySchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
    },
    description: {
        type: String,
        required: true,
    },
    price: {
        type: Number,
        required: true,
    },
    image: {
        type: String,
        required: true,
    }

})

const Property = mongoose.model("Property", PropertySchema)

export default Property